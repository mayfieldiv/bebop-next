    lhs: InlineArray<N, InlineArray<M, Element>>, rhs: InlineArray<N, InlineArray<M, Element>>
) -> Bool {
    for i in 0..<N { for j in 0..<M { if lhs[i][j] != rhs[i][j] { return false } } }
    return true
